void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{

	int16_t enc = __HAL_TIM_GET_COUNTER(&htim4);
	int32_t mot = __HAL_TIM_GET_COUNTER(&htim2);
	int32_t speed = mot-last_mot;
	last_mot=mot;

	txData.startByte = 'NNNN';
	txData.control = enc;
	txData.motor = mot;
	txData.motor_speed = speed;
	txData.endByte = 'EEEE';


	int e = enc - speed;
	if (integral>max_integral)integral=max_integral;
	if (integral<-max_integral)integral=-max_integral;
	integral += e/10;
    HAL_UART_Transmit_DMA (&huart2, &txData, sizeof(txData));

	rotare(integral);
}

